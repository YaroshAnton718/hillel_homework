print("Ця програма обчислює середнє арифметичне трьох чисел")

first_number = float(input("Введіть перше число: "))
second_number = float(input("Введіть друге число: "))
third_number = float(input("Введіть третє число: "))

result = (first_number + second_number + third_number) / 3

print(f"\nСереднє арифметичне цих трьох чисел дорівнює {result}")